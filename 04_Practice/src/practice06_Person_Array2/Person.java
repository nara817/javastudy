package practice06_Person_Array2;

public class Person {
	
	// 펄슨 클래스
	// 펄슨에 이름과 나이
	
	private String name;
	private int age;
	
	
	public Person(String name, int age) {

		this.name = name;
		this.age = age;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	}


	
	

	